using System;
using System.Collections.Generic;
using System.Text;

namespace ApplicationDomain.Common
{
    public static class StringUtil
    {
        public static string RemoveUnicode(string text)
        {
            string[] arr1 = new string[] { "á", "à", "ả", "ã", "ạ", "â", "ấ", "ầ", "ẩ", "ẫ", "ậ", "ă", "ắ", "ằ", "ẳ", "ẵ", "ặ",
    "đ",
    "é","è","ẻ","ẽ","ẹ","ê","ế","ề","ể","ễ","ệ",
    "í","ì","ỉ","ĩ","ị",
    "ó","ò","ỏ","õ","ọ","ô","ố","ồ","ổ","ỗ","ộ","ơ","ớ","ờ","ở","ỡ","ợ",
    "ú","ù","ủ","ũ","ụ","ư","ứ","ừ","ử","ữ","ự",
    "ý","ỳ","ỷ","ỹ","ỵ",};
            string[] arr2 = new string[] { "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a",
    "d",
    "e","e","e","e","e","e","e","e","e","e","e",
    "i","i","i","i","i",
    "o","o","o","o","o","o","o","o","o","o","o","o","o","o","o","o","o",
    "u","u","u","u","u","u","u","u","u","u","u",
    "y","y","y","y","y",};
            for (int i = 0; i < arr1.Length; i++)
            {
                text = text.Replace(arr1[i], arr2[i]);
                text = text.Replace(arr1[i].ToUpper(), arr2[i].ToUpper());
            }
            return text;
        }

        public static string GenerateSearchString(string text)
        {
            text = RemoveUnicode(text);
            text = text.Replace(" ","");
            return text.ToLower();
        }
        public static string ConvertToVND(double value)
        {
            int convertValue = int.Parse(value.ToString().Split('.')[0]);
            int exponent = convertValue.ToString().Length;
            String result = " đ";
            int counter = 0;
            for (int i = 0; i < exponent; i++)
            {
                if (counter == 3)
                {
                    result = '.' + result;
                    counter = 0;
                }
                result = (convertValue % 10).ToString() + result;
                convertValue /= 10;
                counter++;
            }
            return result;
        }
    }
}
